from checkers import print_board, usual_move, one_kill

def test_usual_move():
    # Тест для функции usual_move
    board = [
        [' ', 'w', ' ', 'w', ' ', 'w', ' ', 'w'],
        ['w', ' ', 'w', ' ', 'w', ' ', 'w', ' '],
        [' ', 'w', ' ', 'w', ' ', 'w', ' ', 'w'],
        [' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '],
        [' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '],
        ['b', ' ', 'b', ' ', 'b', ' ', 'b', ' '],
        [' ', 'b', ' ', 'b', ' ', 'b', ' ', 'b'],
        ['b', ' ', 'b', ' ', 'b', ' ', 'b', ' ']
    ]
    x1, y1, x2, y2 = 2, 1, 3, 0  # Пример хода
    usual_move(x1, y1, x2, y2)
    expected_board = [
        [' ', 'w', ' ', 'w', ' ', 'w', ' ', 'w'],
        [' ', ' ', 'w', ' ', 'w', ' ', 'w', ' '],
        ['w', 'w', ' ', 'w', ' ', 'w', ' ', 'w'],
        [' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '],
        [' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '],
        ['b', ' ', 'b', ' ', 'b', ' ', 'b', ' '],
        [' ', 'b', ' ', 'b', ' ', 'b', ' ', 'b'],
        ['b', ' ', 'b', ' ', 'b', ' ', 'b', ' ']
    ]
    assert board == expected_board

